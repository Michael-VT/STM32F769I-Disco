def scale_char(char_id, scale):
    data = font_data.get(char_id, [0x00]*12)
    new_h = 12 * scale
    new_w = 8 * scale
    bytes_per_row = (new_w + 7) // 8
    output = []
    
    for row in range(12):
        val = data[row]
        new_bits = 0
        for b in range(8):
            if (val >> (7-b)) & 1:
                for s in range(scale):
                    new_bits |= (1 << (new_w - 1 - (b * scale + s)))
        
        row_bytes = []
        for i in range(bytes_per_row):
            shift = (bytes_per_row - 1 - i) * 8
            row_bytes.append((new_bits >> shift) & 0xFF)
            
        for _ in range(scale):
            output.extend(row_bytes)
            
    return output

def generate_font_file(name, scale):
    width = 8 * scale
    height = 12 * scale
    header = f'#include "fonts.h"\n\nconst uint8_t {name}_Table[] = {{\n'
    content = ""
    for i in range(32, 127):
        scaled = scale_char(i, scale)
        content += f"  // '{chr(i)}' {i}\n  "
        content += ", ".join([f"0x{b:02X}" for b in scaled])
        content += ",\n"
    footer = f"}};\n\nsFONT {name} = {{ {name}_Table, {width}, {height} }};\n"
    with open(f"Utilities/Fonts/{name.lower()}.c", "w") as f:
        f.write(header + content + footer)
    print(f"Generated {name} ({width}x{height})")

